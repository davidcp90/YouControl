% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
syms t s;
A= get(handles.edit1, 'String');
B= get(handles.edit2, 'String');
C= get(handles.edit3, 'String');
if isempty(A)
 warndlg('Por favor ingrese una funcion','Error');
else
    if isempty(B)&&isempty(C)
        lp=laplace(sym(A));
        r=char(lp);
    
    elseif isempty(B)
        lp=laplace(sym(A),t,sym(C));
        r=char(lp);
    
    elseif isempty(C)
        lp=laplace(sym(A),sym(B),s);
        r=char(lp);
    else
        lp=laplace(sym(A),sym(B),sym(C));
        r=char(lp);
    end
end
          
%sp=simplify(lp);
%pr=pretty(sp);


set(handles.text1,'String',r);



